#include "../minishell.h"

int	ft_isdigit(char *str)
{
	char	*tmp;

	tmp = str;
	while (*tmp)
	{
		if ((*tmp >= 48) && (*tmp <= 57))
			tmp++;
		else
			return (1);
	}
	return (0);
}
